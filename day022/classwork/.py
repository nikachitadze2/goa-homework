# upper() ასოებს გარდაქმნის დიდ ასოებად
name = "nika"
print(name.upper())

# lowercase ასოებს გარდაქმნის პატარა ასოებად
name = "NIKA"
print(name.lower())

#capitalize სიტყვის პირველ ბგერას გარდარქმნის დიდად
